export function validateProposalType(
  hallClass: "I" | "II" | "III",
  type: string,
  inventoryEnabled: boolean = false,
  forgeEnabled: boolean = false
): boolean {
  // Enable proposals are always allowed (hall votes to unlock)
  if (["inventory_enable", "forge_enable", "ihcp_create"].includes(type)) {
    return true;
  }

  // If inventory is enabled, allow inventory-related operational proposals
  if (inventoryEnabled && ["inventory_list", "pricing_change", "marketing", "supplier_change"].includes(type)) {
    return true;
  }

  // If forge is enabled, allow worker-related proposals
  if (forgeEnabled && ["hire", "fire", "performance_review"].includes(type)) {
    return true;
  }

  // Default class-based prohibitions
  const prohibited = getHallClassProhibitions(hallClass);
  return !prohibited.includes(type);
}

