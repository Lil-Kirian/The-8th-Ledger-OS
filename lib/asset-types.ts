export interface RevenueDistribution {
  grossRevenue: number;
  tithePercent: number;
  ihcpRepayment: number;
  payrollReserve: number;
  cogs: number;
  netHallRevenue: number;
  ownerSplit: number;
}

export function calculateRevenueDistribution(
  grossRevenue: number,
  ownershipPercent: number,
  assetTypeId: string,
  workerCount: number = 0,
  ihcpRepayment: number = 0
): RevenueDistribution {
  const asset = getAssetTypeById(assetTypeId);
  const titheRate = getAssetTypeTitheRate(assetTypeId) / 100;
  const titheAmount = Math.round(grossRevenue * titheRate);
  const payrollReserve = asset ? Math.round(grossRevenue * (getAssetTypePayrollReservePercent(assetTypeId) / 100)) : 0;

  let cogs = 0;
  if (asset?.hallClass === "III") {
    cogs = Math.round(grossRevenue * 0.35);
  }

  const netHallRevenue = Math.max(0, grossRevenue - titheAmount - ihcpRepayment - payrollReserve - cogs);
  const ownerSplit = Math.round((netHallRevenue * ownershipPercent) / 100);

  return {
    grossRevenue,
    tithePercent: titheAmount,
    ihcpRepayment,
    payrollReserve,
    cogs,
    netHallRevenue,
    ownerSplit,
  };
}

