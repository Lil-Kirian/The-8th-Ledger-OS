export function generateTransferHash(
  fromLedgerId: string,
  toLedgerId: string,
  hallId: string,
  percentTransferred: number,
  price: number,
  timestamp: string
): string {
  const data = `${fromLedgerId}:${toLedgerId}:${hallId}:${percentTransferred}:${price}:${timestamp}`;
  let hash = 0;
  for (let i = 0; i < data.length; i++) {
    const char = data.charCodeAt(i);
    hash = ((hash << 5) - hash + char) | 0;
  }
  return `LED-${Math.abs(hash).toString(16).toUpperCase().padStart(16, "0")}`;
}

